package com.examReg.model;

public class UserCathi {
	private int id;
	private int userId;
	private int cathiId;
	private int phongthiId;
	private String sbd;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public int getUserId() {
		return userId;
	}
	public void setUserId(int userId) {
		this.userId = userId;
	}
	public int getCathiId() {
		return cathiId;
	}
	public void setCathiId(int cathiId) {
		this.cathiId = cathiId;
	}
	public int getPhongthiId() {
		return phongthiId;
	}
	public void setPhongthiId(int phongthiId) {
		this.phongthiId = phongthiId;
	}
	public String getSbd() {
		return sbd;
	}
	public void setSbd(String sbd) {
		this.sbd = sbd;
	}
	
}
